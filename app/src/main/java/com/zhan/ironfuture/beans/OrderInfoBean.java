public class OrderInfoBean implements Serializable {
    private int orderId;
    private String state;
    private String orderType;
    private String productAddress;//物品所在地 公司id
    private String destination;//目的地     公司id
    private int pickCompanyId;
    private ArrayList<ProductInfo> ProductInfoList=new ArrayList<>();


    public int getPickCompanyId() {
        return pickCompanyId;
    }

    public void setPickCompanyId(int pickCompanyId) {
        this.pickCompanyId = pickCompanyId;
    }

    public int getOrderId() {
        return orderId;
    }

    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public ArrayList<ProductInfo> getProductInfoList() {
        return ProductInfoList;
    }

    public void setProductInfoList(ArrayList<ProductInfo> productInfoList) {
        ProductInfoList = productInfoList;
    }

    public String getProductAddress() {
        return productAddress;
    }

    public void setProductAddress(String productAddress) {
        this.productAddress = productAddress;
    }

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getOrderType() {
        return orderType;
    }

    public void setOrderType(String orderType) {
        this.orderType = orderType;
    }

    public static class ProductInfo implements Serializable{
        private int productId;
        private int productPrice;
        private String goodsName;
        private int productQuantity;

        public int getProductId() {
            return productId;
        }

        public void setProductId(int productId) {
            this.productId = productId;
        }

        public int getProductPrice() {
            return productPrice;
        }

        public void setProductPrice(int productPrice) {
            this.productPrice = productPrice;
        }

        public String getGoodsName() {
            return goodsName;
        }

        public void setGoodsName(String goodsName) {
            this.goodsName = goodsName;
        }

        public int getProductQuantity() {
            return productQuantity;
        }

        public void setProductQuantity(int productQuantity) {
            this.productQuantity = productQuantity;
        }
    }
}
